<?php

$k = 10;
for($i = 1 ; $i <= 50 ; $i++) {
    echo $k."<br/>";
}


// Nhập số n từ bàn phím
$n = 50; // Bạn có thể thay đổi giá trị của n theo ý muốn

// Khởi tạo biến đếm
$i = 1;

// Sử dụng vòng lặp while để in các số từ 1 đến n
while ($i <= $n) {
    echo $i . " ";
    $i++;
}